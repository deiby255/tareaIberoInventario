    public int idCategoria(String categoria)
    {
            int idCategoria = 0;    
        try {
            
            Connection con2 = null;
            PreparedStatement ps2 = null;
            ResultSet rs = null;
            con2 = new BDatos().getConnection();
            String query = "SELECT `ID_CATEGORIA` FROM `categoria` WHERE NOMBRE = ?";
            ps2 = con2.prepareStatement(query);
            ps2.setString(1, categoria);
            rs = ps2.executeQuery();
            while(rs.next())
            {
                idCategoria = rs.getInt(1);
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(Mantenimiento.class.getName()).log(Level.SEVERE, null, ex);
        }
         return idCategoria;
    }
    
